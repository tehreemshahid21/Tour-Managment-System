    private void b1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b1ActionPerformed
        if(t1.getText().isEmpty()||t2.getText().isEmpty()||t3.getText().isEmpty()||t4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "missing information");
        }
        else
        {
            try
            {
                countpassengers();
                con=DriverManager.getConnection("jdbc:mysql://localhost:3307/tourdb","root","arsenal360");
                PreparedStatement add=con.prepareStatement("insert into passengertable values(?,?,?,?,?,?,?)");
                add.setInt(1,passid);
                add.setString(2, t1.getText());
                add.setString(3,Pstatuscb.getSelectedItem().toString());
                add.setString(4,Pgendercb.getSelectedItem().toString());
                add.setString(5,t2.getText());
                add.setString(6, t3.getText());
                add.setString(7, t4.getText());
                int row=add.executeUpdate();
                JOptionPane.showMessageDialog(this, "passenger is added");
                con.close();
                displayPassengers();
                clear(); 
            }catch(Exception e){   
                 JOptionPane.showMessageDialog(this, e );
            }
        } 
    }//GEN-LAST:event_b1ActionPerformed

