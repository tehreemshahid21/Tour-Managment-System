    private void displayPassengers()
    {
        try
        {
            con=DriverManager.getConnection("jdbc:mysql://localhost:3307/tourdb","root","arsenal360");
            s=con.createStatement();
            rs=s.executeQuery("select * from passengertable");
            passengertable.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            
        }
        
    }
    int passid=0;
    private void countpassengers()
    {
        try
        {
            s1=con.createStatement();
            rs1=s1.executeQuery("select MAX(Pid) from passengertable");
            rs1.next(); 
            passid=rs1.getInt(1)+1; 
        }catch(Exception e){
            
        }    
    }
